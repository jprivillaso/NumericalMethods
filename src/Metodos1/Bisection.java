    public void biseccion(double xi, double xs, double tolerancia, int nIter, boolean errorAbs, boolean errorRela) {
        double fxi = listFunc.f(xi);
        double fxs = listFunc.f(xs);
        int row = 0;
        Object[][] matriz = new Object[nIter+1][5];
        matriz[row][0] = xi;
        matriz[row][1] = xs;
        System.out.println(fxi);
         System.out.println(fxs);
        if (fxi == 0) {
            jTextArea1.removeAll();
            jTextArea1.setForeground(Color.BLACK);
            jTextArea1.setText("The root is : " + xi);
        } else if (fxs == 0) {
            jTextArea1.removeAll();
            jTextArea1.setForeground(Color.BLACK);
            jTextArea1.setText("The root is : " + xs);
        } else if (fxi * fxs < 0) {
            double xm = (xi + xs) / 2;
            double fxm = listFunc.f(xm);
            int counter = 1;
            double error = tolerancia + 1;
            matriz[row][2] = xm;
            matriz[row][3] = fxm;
           // matriz[row][4] = error;
            row++;
                      
            while (error > tolerancia && fxm != 0 && counter < nIter) {
                if (fxi * fxm < 0) {
                    xs = xm;
                    fxs = fxm;
                } else {
                    xi = xm;
                    fxi = fxm;
                }
                double xaux = xm;
                xm = (xi + xs) / 2;
                fxm = listFunc.f(xm);
                if(errorAbs == true){
                    error = Math.abs(xm - xaux);
                }
                if(errorRela == true){
                    error = Math.abs((xm - xaux)/xm);
                } 
                
                matriz[row][1] = xi;
                matriz[row][0] = xs;
                matriz[row][2] = xm;
                matriz[row][3] = fxm;
                matriz[row][4] = error;
                
                row++;                
                counter++;
            }           
            
            
            if (fxm == 0) {
                jTextArea1.removeAll();
                jTextArea1.setForeground(Color.BLACK);
                jTextArea1.setText("La raiz es= " + xm);
            } else if (error <= tolerancia) {
                jTextArea1.removeAll();
                jTextArea1.setForeground(Color.BLACK);
                jTextArea1.setText(xm + " es una aproximacion a la raiz con tolerancia de " + tolerancia);
            } else {
                jTextArea1.removeAll();
                jTextArea1.setForeground(Color.BLACK);
                jTextArea1.setText("El metodo fallo en " + nIter + " iteraciones.");
                
            }
        } else {
            
            jTextArea1.removeAll();
            jTextArea1.setForeground(Color.BLACK);
            jTextArea1.setText("Se ingreso un intervalo incorrecto");
        }
        jTable1.setModel(new javax.swing.table.DefaultTableModel(matriz, new String[]{"xi", "xs","xm","fxm","error"}));

    }

    
